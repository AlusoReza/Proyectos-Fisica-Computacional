      SUBROUTINE MOMENPLAN

!     EN EL MISMO PROGRAMA SE VA A DEMOSTRAR QUE EL MOMENTO SE MANTIENE CONSTANTE CALCULANDO SU VALOR Y SUMANDOLO PARA TODOS LOS MOMENTOS EN LOS QUE SE HA CALCULADO LA POSICION Y VELOCIDAD. EL MOMENTO LO OBTENEMOS COMO EL PRODUCTO VECTORIAL ENTRE EL VECTOR DIRECTOR Y EL VECTOR MOMENTO LINEAL Q NO ES MAS Q LA MASA DE CADA PLANETA POR EL VECTOR VELOCIDAD.
      REAL*8, DIMENSION(1:10):: SUMMOM
      REAL*8, DIMENSION(1:10, 1:DIV):: MOM

      SUMMOM(1) = 0.D0
      
      DO I = 2, 10
         SUMMOM(I) = 0.D0
         DO K = 1, DIV
            MOM(I, K) = MD(I)*(RV(I,1,K)*VV(I,2,K)-RV(I,2,K)*VV(I,1,K))
            SUMMOM(I) = SUMMOM(I) + MOM(I,K) 
         END DO   
      END DO
      
      WRITE(6,*) " "
      WRITE(6,*) "2. "
      WRITE(6,*) "LA SUMA DE MOMENTOS PARA CADA UNO DE LOS PLANETAS ES:"
