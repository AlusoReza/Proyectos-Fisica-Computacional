      END SUBROUTINE MOMENPLAN
      
      SUBROUTINE ENERPLAN
!     TMB EN EL MISMO PROGRAMA VAMOS COMPROBAR QUE LA ENERGIA SE MANTIENE CONSTANTE, PARA ELLO VAMOS A CALCULAR SU VALOR PARA CADA PLANETA Y SUMARLO, Y MOSTRARLOS POR PANTALLA. LA ENERGÍA CINÉTICA ES LA QUE SIEMPRE SE CALCULA, COMO UN MEDIO DE LA MASA DEL CORRESPONDIENTE PLANETA POR EL MODULO DEL VECTOR VELOCIDAD AL CUADRADO. LA ENERGÍA POTENCIAL ES LA PROCEDENTE DE LA FUERZA TOTAL COMO GRADIENTE DE UN POTENCIAL, POR TANTO TOMA UNA FORMA MUY PARECIDA A LA ECUACION UTILIZADA PARA OBTENER LA ACELERACION. LA ENERGIA TOTAL ES LA SUMA DE LAS DOS ANTERIORES.
      INTEGER*8:: L
      REAL*8:: PO, MODR
      REAL*8, DIMENSION(1:10):: ET
      REAL*8, DIMENSION(1:10,1:DIV):: EC, U

      DO I = 2, 10
         ET(I) = 0.D0
         DO K = 1, DIV
            DO L=1, 10
               PO = 0.D0
               MODR = 0.D0
               IF (I.NE.L) THEN
                MODR = (RV(L,1,K)-RV(I,1,K))**2+(RV(L,2,K)-RV(I,2,K))**2
                PO = G*(MD(I)*MD(K))/(SQRT(MODR))
               END IF   
               U(I,K)=U(I,K) + PO
            END DO   
            EC(I,K)=(M(I)/2)*((VV(I,1,K))**2+(VV(I,2,K))**2)

            ET(I) = ET(I) + U(I,K) + EC(I,K)
         END DO   
      END DO

      WRITE(6,*) " "
      WRITE(6,*) "3. "
      WRITE(6,*) "LA SUMA DE ENERGIA PARA CADA UNO DE LOS PLANETAS ES:"
      WRITE(6,*) " "
      WRITE(6,*) "- PARA MERCURIO:", ET(2)
      WRITE(6,*) "- PARA VENUS:", ET(3)
      WRITE(6,*) "- PARA TIERRA:", ET(4)
      WRITE(6,*) "- PARA MARTE:", ET(5)
      WRITE(6,*) "- PARA JUPITER:", ET(6)
      WRITE(6,*) "- PARA SATURNO:", ET(7)
      WRITE(6,*) "- PARA URANO:", ET(8)
      WRITE(6,*) "- PARA NEPTUNO:", ET(9)
      WRITE(6,*) "- PARA PLUTON:", ET(10)

      END SUBROUTINE ENERPLAN
      
