      SUBROUTINE EVOLVERLET
      
!     AUNQUE PODAMOS INTRODUCIR UNIDADES DE TIEMPO EN NUMEROS REALES SE VA A CONSIDERAR LAS DIVISIONES DEL TIEMPO Y EL PASO DEL TIEMPO EN UNIDADES ENTERAS CASI QUE OBVIANDO LOS DECIMALES YA QUE EN REALIDAD DADO LAS GRANDES MAGNITUDES DE TIEMPO EN LAS QUE SE VA A TRABAJAR CASI QUE LOS DECIMALES DAN IGUAL.
      INTEGER*8:: L
      REAL*8::  MODR, MODR2
      INTEGER*8, DIMENSION(1:2):: SUMA
      REAL*8, DIMENSION(:,:,:), ALLOCATABLE:: ALPHA

      ALLOCATE(RV(1:10,1:2,1:DIV))
      ALLOCATE(VV(1:10,1:2,1:DIV))
      ALLOCATE(A(1:10,1:2,1:DIV))
      ALLOCATE(RG(1:10,1:2,1:DIV))
      ALLOCATE(ALPHA(1:10,1:2,1:DIV))
      ALLOCATE(RVD(1:10,1:2,1:DIV))

!     DADO AL GRAN TAMAÑO DE LOS NUMEROS LOS REDIMENSIONAMOS PARA REALIZAR LOS CALCULOS CORRESPONDIENTES CORRECTAMENTE UTILIZANDO EL ALGORITMO DE VERLET A LO LARGO DEL RESTO DEL PROGRAMA. REDIMENSIONAREMOS: M, R, T Y H. DE ESTA FORMA Y COMO VAMOS A MOSTRAR A CONTINUACIÓN SE VA A TENER LOS DATOS SIN REDIMENSIONAR EN LOS VECTORES M, R..., LOS VECTORES REDIMENSIONADOS MD, RD... Y POR ÚLTIMO LOS QUE SE VAN A USAR PARA REALIZAR CÁLCULOS RV Y VV.
!     RECORDEMO QUE VECTORIALMENTE LOS VECTORES POSICION Y VELOCIDAD SON PERPENDICULARES. 1 - DIRECCIÓN DE X Y 2 - DIRECCION DE Y.
      DO I=2, 10
         RD(I) = R(I)/C
         MD(I) = M(I)/MS
         VD(I) = V(I)/((G*MS/C)**(0.5D0))
         RV(I,1,1) = RD(I)
         VV(I,2,1) = VD(I)
!        VV(I,2,1) = (G*MS/RD(I))**(0.5D0)
         RV(I,2,1) = 0.D0   
         VV(I,1,1) = 0.D0
!        WRITE(6,*) I,RD(I),MD(I),RV(I,1,1),VV(I,2,1),RV(I,2,1),VV(I,1,1)
      END DO
      
      TD = ((G*MS/C**3)**(0.5D0)) * (T*3600)
      HD = ((G*MS/C**3)**(0.5D0)) * (H*3600)
!      WRITE(6,*) TD, HD, TD/HD

!     EL SOL SIEMPRE SE MANTIENE EN SU POSICIÓN INICIAL:
