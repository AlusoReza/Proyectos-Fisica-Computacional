         DO K = 1, DIV
            RV(1, J, K) = 0.D0
            VV(1, J, K) = 0.D0
            A(1, J, K) = 0.D0
         END DO   
      END DO   
      
!     EL ALGORITMO DE VERLET ES EL SIGUIENTE, PARA EL MODELO HELIOCÉNTRICO:
      
!     A CADA POSICIÓN INICIAL SE LE ASIGNA UNA ACELERACIÓN INICIAL.
  !     POR CADA INSTANTE DE TIEMPO ENCONTRAMOS QUE LA ACELERACIÓN VARIA SIGUIENDO LA ECUACIÓN SUMATORIA DESCRITA EN PLANETAS-SLIDESV1.PDF.
!     SE VA A UTILIZAR DE MANERA AUXILIAR UN REAL CONOCIDO COMO SUMA QUE CUATIFICARA LA SUMA DE LOS VECTORES EN LA ACELERACIÓN Y SE REINICIARÁ PARA CADA CICLO DE TIEMPO K, Y PARA CADA CICLO DEL SUMATORIO:
