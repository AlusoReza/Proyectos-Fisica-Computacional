         DO J = 1, 2
            A(I, J, 1) = 0.D0
         END DO   
         
         DO L = 1, 10
        
            IF (L.NE.I) THEN
               
               MODR=(RV(I,1,1)-RV(L,1,1))**2+(RV(I,2,1)-RV(L,2,1))**2
               MODR2 = MODR**1.5D0
               
               DO J = 1, 2 
             A(I, J, 1)=A(I, J, 1)-(MD(L)*(RV(I,J,1)-RV(L,J,1)))/(MODR2)
               END DO
          
            END IF

            MODR = 0.D0
            MODR2 = 0.D0
            
         END DO
         WRITE(6,*) A(I,1,1), A(I,2,1)
      END DO
      
       
      
!     -------------------------------------------------------------------------------------------------------------------------
!     EN ESTA PARTE SE HA PROBADO UN ALGORTIMO QUE CALCULA LA ACELERACIÓN COMO FUERZA GRAVITATORIA PROCEDENTE UNICAMENTE POR EL SOL PARA ENCONTRAR EL ERROR.
!      DO I = 2, 10
!          DO J = 1, 2
!      A(I, J, 1) =-MS*G*RV(I,J,1)/((RV(I,1,1)**2+RV(I,2,1)**2)**(1.5D0))
!         END DO   
!      END DO   
 
!     -------------------------------------------------------------------------------------------------------------------------         
!     SE GUARDAN LAS VARIACIONES DE LAS MAGNITUDES A CADA VARIACION DE TIEMPO CONOCIDA POR EL VECTOR TT.
!        WRITE(6,*) I,RD(I),MD(I),RV(I,1,1),VV(I,2,1),RV(I,2,1),VV(I,1,1)
      DO K=1, DIV - 1

!     PRIMERO EVALUMOS RV Y ALPHA CONOCIENDO VV Y A INICIALES:
         DO I = 2, 10
            DO J = 1, 2
             ALPHA(I,J,K) = VV(I,J,K) + (HD/2.D0)*A(I, J,K)
             RV(I,J, K+1) = RV(I,J, K) + ALPHA(I,J,K)*HD
            END DO
         END DO 
!            WRITE(6,*) I, K, RV(I,1, K), RV(I,2, K)
!            WRITE(6,*) I, K, RV(I,1, K+1), RV(I,2, K+1)

!     OBTENEMOS LA SIGUIENTE ACELERACIÓN Y LA NUEVA VELOCIDAD EN EL SIGUIENTE CICLO:

         DO I = 2, 10
         
         DO J = 1, 2
            A(I, J, K+1) = 0.D0
         END DO   
         
         DO L = 1, 10
        
            IF (L.NE.I) THEN
          MODR=(RV(I,1,K+1)-RV(L,1,K+1))**2+(RV(I,2,K+1)-RV(L,2,K+1))**2
          MODR2 = MODR**1.5D0
               DO J = 1, 2
         A(I,J,K+1)=A(I,J,K+1)-(MD(L)*(RV(I,J,K+1)-RV(L,J,K+1)))/(MODR2)
               END DO
            END IF

            MODR = 0.D0
            MODR2 = 0.D0
            
         END DO
         END DO
         
         DO I = 2,10
            DO J = 1,2
            VV(I,J,K+1) = ALPHA(I, J, K) + (HD/2.D0)*A(I, J, K+1)
            END DO
         END DO
         
      END DO
         
 

!     HAY QUE TENER EN CUENTA QUE TODAS LAS POSICIONES FUERON ANTERIORMENTE REESCALADAS, POR ESO PRIMERO VAMOS A HACER EL PROCESO CONTRARIO PARA OBTENER VALORES REALES DE LAS CORRESPONDIENTES MEDIDAS.

      DO I = 1, 10
         DO J = 1, 2
            DO K = 1, DIV
               RVD(I,J,K)=RV(I,J,K) * C
            END DO
         END DO   
      END DO
      
!     PARA EL MODELO GEOCÉNTRICO DEL MOVIMIENTO DE LOS PLANETAS SIMPLEMENTE SE VA A CONSIDERAR QUE LA TIERRA ES EL CENTRO DE MOVIMIENTO, Y ESTO ES FACILMENTE OBTENIBLE RESTANDOLE LA DISTANCIA TIERRA-SOL A TODAS LAS POSICIONES PLANETARIAS. AUNQUE HAY QUE TENER EN CUENTA QUE ESTA ES VARIABLE Y POR TANTO SE TENDRAN EN CONSIDERACIONES LAS MISMAS VARIACIONES GUARDADAS EN EL VECTOR RV(I,J) UTILIZADO EN EL ALGORITMO DE VERLET, REALIZANDO DE NUEVO EL MISMO PERO CON LAS NUEVAS POSICIONES.
      DO I = 1, 10
         DO J = 1, 2
            DO K = 1, DIV
               RG(I, J, K) = RVD(I, J, K) - R(4)
            END DO   
         END DO
      END DO   

!     PARA EL MOVIMIENTO CENTRADO EN EL SOL.
      OPEN(UNIT = 20, FILE = 'VXY-MovHelSOL.dat', STATUS = 'UNKNOWN')
      DO K = 1, DIV
         WRITE(20,*) RVD(1, 1, K), RVD(1, 2, K)   
      END DO   
