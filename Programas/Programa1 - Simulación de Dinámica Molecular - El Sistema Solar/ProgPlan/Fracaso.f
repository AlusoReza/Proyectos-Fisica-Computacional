!     EN EL SIGUIENTE VECTOR SE VA A GUARDAR LOS VALORES DE X E Y CALCULADOS EN EL ALGORITMO DE VERLET MÁS GRANDE A LO LARGO DE TODOS LOS CALCULADOS.
